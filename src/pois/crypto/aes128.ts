// Round constants for key expansion (RCON[i] = 2^(i-1) in GF(2^8))
const RCON = new Uint8Array([
  0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36,
])

// GF(2^8) multiplication by 2 (xtime)
function xtime(b: number): number {
  return ((b << 1) ^ (b & 0x80 ? 0x1b : 0x00)) & 0xff
}

// GF(2^8) multiply a by b
function gmul(a: number, b: number): number {
  let p = 0
  let hi: number
  for (let i = 0; i < 8; i++) {
    if (b & 1) p ^= a
    hi = a & 0x80
    a = (a << 1) & 0xff
    if (hi) a ^= 0x1b
    b >>= 1
  }
  return p
}

/**
 * AES-128 key expansion.
 * Returns 176 bytes = 11 round keys × 16 bytes each.
 */
export function expandKey(key: ByteArray): Uint8Array {
  const w = new Uint8Array(176)
  // First round key = the original key
  w.set(key.slice(0, 16))

  for (let i = 16; i < 176; i += 4) {
    const prev = w.slice(i - 4, i)
    let temp = new Uint8Array(4)

    if ((i / 4) % 4 === 0) {
      // RotWord: [a0,a1,a2,a3] -> [a1,a2,a3,a0]
      temp[0] = SBOX[prev[1]] ^ RCON[(i / 16) - 1]
      temp[1] = SBOX[prev[2]]
      temp[2] = SBOX[prev[3]]
      temp[3] = SBOX[prev[0]]
    } else {
      temp = prev.slice()
    }

    for (let j = 0; j < 4; j++) {
      w[i + j] = w[i - 16 + j] ^ temp[j]
    }
  }

  return w
}

/**
 * AES-128 block encryption.
 * block: 16-byte plaintext
 * roundKeys: 176 bytes from expandKey()
 * Returns: 16-byte ciphertext
 */
export function aesEncryptBlock(block: ByteArray, roundKeys: Uint8Array): ByteArray {
  // State is 4x4 column-major: state[col*4 + row]
  const s = new Uint8Array(16)
  for (let i = 0; i < 16; i++) s[i] = block[i] ?? 0

  // AddRoundKey (round 0)
  for (let i = 0; i < 16; i++) s[i] ^= roundKeys[i]

  for (let round = 1; round <= 10; round++) {
    // SubBytes
    for (let i = 0; i < 16; i++) s[i] = SBOX[s[i]]

    // ShiftRows: row r is shifted left by r positions
    // Row 0: no shift
    // Row 1: [s[1],s[5],s[9],s[13]] -> shift left 1
    let t = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = t
    // Row 2: shift left 2
    t = s[2]; s[2] = s[10]; s[10] = t
    t = s[6]; s[6] = s[14]; s[14] = t
    // Row 3: shift left 3 (= shift right 1)
    t = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = s[3]; s[3] = t

    if (round < 10) {
      // MixColumns: operate on each of the 4 columns
      for (let c = 0; c < 4; c++) {
        const i = c * 4
        const s0 = s[i], s1 = s[i+1], s2 = s[i+2], s3 = s[i+3]
        s[i]   = gmul(0x02, s0) ^ gmul(0x03, s1) ^ s2 ^ s3
        s[i+1] = s0 ^ gmul(0x02, s1) ^ gmul(0x03, s2) ^ s3
        s[i+2] = s0 ^ s1 ^ gmul(0x02, s2) ^ gmul(0x03, s3)
        s[i+3] = gmul(0x03, s0) ^ s1 ^ s2 ^ gmul(0x02, s3)
      }
    }

    // AddRoundKey
    const rk = round * 16
    for (let i = 0; i < 16; i++) s[i] ^= roundKeys[rk + i]
  }

  return s
}

/**
 * Convenience: AES-128 encrypt one block with a raw key.
 * key: exactly 16 bytes (zero-pads if shorter)
 * plaintext: exactly 16 bytes (zero-pads if shorter)
 */
export function aes128(key: ByteArray, plaintext: ByteArray): ByteArray {
  const k = new Uint8Array(16)
  k.set(key.slice(0, 16))
  const p = new Uint8Array(16)
  p.set(plaintext.slice(0, 16))
  return aesEncryptBlock(p, expandKey(k))
}

